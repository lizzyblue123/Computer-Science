package lizzy;

public class MathMethods{
	public static void main(String args[]){
		System.out.println("Sqrt of 9 =   "+ Math.sqrt(9));
		System.out.println("16^4 =   "+ Math.pow(16, 4));
		System.out.println("Absolute value of -369.5 =   "+ Math.abs(-369.5));
		System.out.println("Round up 14.2 =   "+ Math.ceil(14.2));
		System.out.println("Round down 14.9 =   "+ Math.floor(14.9));
		System.out.println("Max of 34.2 or 43.2 =   "+ Math.max(34.2, 43.2));
		System.out.println("Min of 52.3 or 25.3 =   "+ Math.min(52.3, 25.3));
	}
}